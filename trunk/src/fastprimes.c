/* subroutine returns 1 if the number is in the table of prime numbers up to 65537 */
unsigned primes_table_check(unsigned p)
{
	unsigned index;
	unsigned field;
	if (p==2) return 1;
	if (p<2 || p>primes_table_limit || (p & 1) == 0) return 0;
	p = (unsigned) (p/2);
	index = (unsigned)(p/32);
	field = p%32;
	return ((primes_table[index] & (1 << field))==0) ? 0 : 1;
}
